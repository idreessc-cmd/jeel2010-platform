// Initialize quizzes list in localStorage if it doesn't exist
export const initializeQuizzes = () => {
    let quizzes = storage.get(QUIZZES_KEY);
    if (!quizzes) {
        quizzes = initialQuizzes;
        storage.set(QUIZZES_KEY, quizzes);
    }
    return quizzes;
};

