// Admin helper function to add questions dynamically to a lesson's quiz
export const addQuestionToQuiz = (lessonId, question) => {
    const quizzes = initializeQuizzes();
    
    // Check if this lesson has a quiz, if not create it
    if (!quizzes[lessonId]) {
        quizzes[lessonId] = {
            lessonId: lessonId,
            title: `اختبار تقييمي مخصص للدرس`,
            questions: []
        };
    }
    
    const questionsList = quizzes[lessonId].questions;
    const newId = questionsList.length > 0 ? Math.max(...questionsList.map(q => q.id)) + 1 : 1;
    
    quizzes[lessonId].questions.push({
        id: newId,
        text: question.text,
        options: question.options,
        correctAnswer: question.correctAnswer
    });
    
    storage.set(QUIZZES_KEY, quizzes);
    return { success: true };
};
